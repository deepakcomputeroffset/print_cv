/**
 * Extracts the base name (e.g., "VC-01" or "Design_209") from a URL.
 * @param {string} url - The full URL string.
 * @returns {string} The extracted name.
 */
const getNameFromUrl = (url) => {
    // Gets the full filename from the URL path
    const filename = url.substring(url.lastIndexOf("/") + 1);
    // Gets the part of the filename before the last underscore
    const name = filename.substring(0, filename.lastIndexOf("_"));
    return name;
};

